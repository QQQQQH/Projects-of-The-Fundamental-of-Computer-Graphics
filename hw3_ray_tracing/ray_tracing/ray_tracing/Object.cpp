#include "Object.h"

void Object::set_model(const glm::mat4& model0) {
	model = model0;
}

void Cube::set_up() {
	glGenVertexArrays(1, &VAO);
	glGenBuffers(1, &VBO);

	glBindVertexArray(VAO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);

	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*) 0);
	glEnableVertexAttribArray(0);
	// normal attribute
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*) (3 * sizeof(float)));
	glEnableVertexAttribArray(1);

	glBindVertexArray(0);
}

Cube::Cube() {
	set_up();
}

void Cube::prepare_for_ray_tracing() {
	for (int i = 0; i < 12; ++i) {
		for (int j = 0; j < 3; ++j) {
			faces[i].points[j] = glm::vec3(
				vertices[i * 9 + j * 3],
				vertices[i * 9 + j * 3 + 1],
				vertices[i * 9 + j * 3 + 2]);
		}
		auto
			ab = faces[i].points[1] - faces[i].points[0],
			ac = faces[i].points[2] - faces[i].points[0];
		faces[i].norm = glm::normalize(glm::cross(ab, ac));
	}
}

tuple<float, const Object*, glm::vec3> Cube::get_intersection(const Ray& ray) {
	float minT = INF;
	const Face* collidedFace = nullptr;
	glm::vec3 start = ray.vertex, direction = ray.direction;
	glm::vec3 norm;
	for (const auto& face : faces) {
		float v1 = glm::dot(face.points[0] - start, face.norm);
		float v2 = glm::dot(face.norm, direction);
		if (abs(v2) > EPS && v1 / v2 > EPS) { // v2 != 0 && t >= 0
			float t = v1 / v2;
			if (t < minT) {
				glm::vec3 p = ray.point_at_t(t);
				if (face.in_face(p)) {
					minT = t;
					collidedFace = &face;
				}
			}
		}
	}
	if (collidedFace) {
		norm = collidedFace->norm;
	}
	return make_tuple(minT, this, norm);
}

void Cube::Draw(Shader& shader) {
	shader.use();
	glBindVertexArray(VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}

